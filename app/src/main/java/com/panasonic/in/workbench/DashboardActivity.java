    private class LogoutClickListener implements View.OnClickListener {
        @Override
        public void onClick(View v) {
            MockTestApplication.getInstance()
                    .getGoogleSignInClient()
                    .signOut()
                    .addOnCompleteListener(DashboardActivity.this, new OnCompleteListener<Void>() {
                        @Override
                        public void onComplete(@NonNull Task<Void> task) {
                            finish();
                        }
                    });
        }
    }



